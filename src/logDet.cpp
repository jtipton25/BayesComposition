#include <RcppArmadillo.h>

// [[Rcpp::depends(RcppArmadillo)]]
// [[Rcpp::interfaces(r, cpp)]]

using namespace Rcpp;
using namespace arma;

///////////////////////////////////////////////////////////////////////////////
//////////////// log determinant of Covariance matrix Sigma  //////////////////
///////////////////////////////////////////////////////////////////////////////

// [[Rcpp::export]]
double logDet(const arma::mat Sig) {
    arma::mat rooti = chol(Sig);
    return(2.0 * sum(log(rooti.diag())));
}

///////////////////////////////////////////////////////////////////////////////
/////// log determinant of Cholesky factor of Covariance matrix Sigma  ////////
///////////////////////////////////////////////////////////////////////////////


// [[Rcpp::export]]
double logDetChol(const arma::mat Sig_chol) {
  return(2.0 * sum(log(Sig_chol.diag())));
}
